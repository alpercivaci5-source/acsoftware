export function t(lang: Language, key: string) {
  return translations[lang]?.[key] ?? translations[defaultLang]?.[key] ?? key;
}

export function isLanguage(value: string | undefined | null): value is Language {
  return !!value && languages.includes(value as Language);
}

export function resolveLanguage(value: string | undefined | null): Language {
  return isLanguage(value) ? value : defaultLang;
}
